    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        final Context c = getActivity().getApplicationContext();
        final View view = inflater.inflate(R.layout.fragment_dashboard, container, false);
        MaterialRippleLayout SCAN;
        SCAN = view.findViewById(R.id.ssss);
        SCAN.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {

               if (internetAvailable())
                {
                    Intent intent = new Intent(c,ClientList.class);
                    startActivity(intent);
                }
                else
                {
                   showCustomDialog();
                }
            }
        });



        return view;
    }
    public boolean internetAvailable() {
        boolean haveWiFi = false;
        boolean haveMobile = false;
        ConnectivityManager connectivityManager = (ConnectivityManager) getActivity().getSystemService(Context.CONNECTIVITY_SERVICE);
        NetworkInfo[] networkInfos = connectivityManager.getAllNetworkInfo();
        for (NetworkInfo networkInfo : networkInfos) {
            if (networkInfo.getTypeName().equalsIgnoreCase("WiFi")) {
                if (networkInfo.isConnected()) {
                    haveWiFi = true;
                }
            }
            if (networkInfo.getTypeName().equalsIgnoreCase("MOBILE")) {
                if (networkInfo.isConnected()) {
                    haveMobile = true;
                }
            }
        }
        return haveWiFi || haveMobile;
    }

    private void showCustomDialog() {
        final Dialog dialog = new Dialog(getActivity());
        dialog.requestWindowFeature(Window.FEATURE_NO_TITLE); // before
        dialog.setContentView(R.layout.dialog_warning);
        dialog.setCancelable(true);

        WindowManager.LayoutParams lp = new WindowManager.LayoutParams();
        lp.copyFrom(dialog.getWindow().getAttributes());
        lp.width = WindowManager.LayoutParams.WRAP_CONTENT;
        lp.height = WindowManager.LayoutParams.WRAP_CONTENT;


        ((AppCompatButton) dialog.findViewById(R.id.bt_close)).setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {

                        if (internetAvailable())
                        {
                            Intent intent = new Intent(getActivity(),ClientList.class);
                            startActivity(intent);
                        }
                        else
                        {
                            showCustomDialog();
                        }

                               dialog.dismiss();
            }
        });

        ((AppCompatButton) dialog.findViewById(R.id.bt_set)).setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {

                dialog.dismiss();
            }
        });

        dialog.show();
        dialog.getWindow().setAttributes(lp);
    }


